package day1013;
// 기본형 데이터타입04
// 논리형 데이터타입 boolean

// boolean의 경우, 딱 두가지 값만 존재한다.
// ture/false의 값이 그 두가지 값인데,
// 우리가 boolean 데이터타입은 변수를 만들어서 쓰기보다는
// 나중에 배우게 될 연산자 혹은 메소드의 결과값을 그대로 쓰는 경우가 많다.

public class Ex02Boolean {
    public static void main(String[] args) {
        // boolean 변수 myBool을 선언해보자
        boolean myBool;
        // myBool에 true를 저장해보자
        myBool = true;
        // myBool의 현재값을 출력해보자
        System.out.println(myBool);
        // myBool에 false를 저장해보자
        myBool = false;
        // myBool의 현재값을 출력해보자
        System.out.println(myBool);
    }

}
